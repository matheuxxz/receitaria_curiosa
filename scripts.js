function LeiaMais() {

    var botao = document.getElementById("btn");
    var mais = document.getElementById("mais");
    var pontos = document.getElementById("pontos");


    if(mais.style.display='none') {
        mais.style.display='inline';
        pontos.style.display='none';
    } 

}

